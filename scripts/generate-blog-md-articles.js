// Build-time generator: scan content/blog/*.md, parse frontmatter + body,
// emit lib/blog-articles-from-md.generated.ts.
//
// Why a generator (not runtime fs):
// - lib/blog-articles.ts is imported by pages/blog.tsx at MODULE TOP LEVEL,
//   so it ends up in the client bundle. We can't `import fs from 'fs'` there
//   without breaking the build. Pre-generating a pure JS data module avoids
//   any runtime fs and keeps the client bundle clean.
// - This runs once per build (`npm run build`) and on demand via
//   `npm run generate-md-articles`.
//
// Frontmatter parser: minimal YAML — supports `key: "value"`, `key: null`,
// and `key:\n  - "item"` lists. Sufficient for the SEO-finalizer output
// shape. Throws on anything outside this grammar to surface schema drift.

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const REPO_ROOT = path.resolve(__dirname, '..');
const CONTENT_DIR = path.join(REPO_ROOT, 'content/blog');
const OUTPUT_FILE = path.join(REPO_ROOT, 'lib/blog-articles-from-md.generated.ts');

// ─── Minimal YAML parser ─────────────────────────────────────────────────

function unquote(s) {
  // Strip surrounding double quotes and unescape \" and \\
  if (s.startsWith('"') && s.endsWith('"')) {
    return s.slice(1, -1).replace(/\\"/g, '"').replace(/\\\\/g, '\\');
  }
  // Bare scalar
  if (s === 'null') return null;
  if (s === 'true') return true;
  if (s === 'false') return false;
  if (/^-?\d+(\.\d+)?$/.test(s)) return Number(s);
  return s;
}

function parseYaml(text) {
  const result = {};
  const lines = text.split('\n');
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (line.trim() === '' || line.startsWith('#')) {
      i++;
      continue;
    }
    // Scalar: key: value
    const scalarMatch = line.match(/^([A-Za-z_][A-Za-z0-9_]*):\s*(.*)$/);
    if (!scalarMatch) throw new Error(`Unrecognized YAML line: ${JSON.stringify(line)}`);
    const key = scalarMatch[1];
    const valueRaw = scalarMatch[2].trim();
    if (valueRaw === '') {
      // List follows (or block scalar — we only support list here)
      const items = [];
      i++;
      while (i < lines.length && lines[i].match(/^\s+-\s+/)) {
        const itemMatch = lines[i].match(/^\s+-\s+(.*)$/);
        items.push(unquote(itemMatch[1].trim()));
        i++;
      }
      result[key] = items;
      continue;
    }
    if (valueRaw === '[]') {
      result[key] = [];
      i++;
      continue;
    }
    result[key] = unquote(valueRaw);
    i++;
  }
  return result;
}

// ─── Required field check ────────────────────────────────────────────────

const REQUIRED_FIELDS = [
  'id', 'slug', 'title', 'excerpt', 'metaDescription',
  'category', 'cluster', 'date', 'dateModified', 'readTime',
  'tags', 'authorKey', 'relatedSlugs',
];

function validateArticle(article, file) {
  for (const f of REQUIRED_FIELDS) {
    if (article[f] === undefined || article[f] === null) {
      throw new Error(`${file}: missing required frontmatter field "${f}"`);
    }
  }
  if (!/^[a-z0-9-]{1,100}$/.test(article.slug)) {
    throw new Error(`${file}: invalid slug "${article.slug}" (must match ^[a-z0-9-]{1,100}$)`);
  }
}

// ─── Main ────────────────────────────────────────────────────────────────

function main() {
  if (!fs.existsSync(CONTENT_DIR)) {
    console.log(`[generate-blog-md-articles] content/blog/ does not exist — emitting empty registry`);
    writeGeneratedFile([]);
    return;
  }

  const files = fs.readdirSync(CONTENT_DIR).filter(f => f.endsWith('.md'));
  if (files.length === 0) {
    console.log(`[generate-blog-md-articles] no .md files found — emitting empty registry`);
    writeGeneratedFile([]);
    return;
  }

  const articles = [];
  for (const file of files) {
    const filePath = path.join(CONTENT_DIR, file);
    const raw = fs.readFileSync(filePath, 'utf8');
    const m = raw.match(/^---\n([\s\S]+?)\n---\n([\s\S]*)$/);
    if (!m) {
      throw new Error(`${file}: no valid frontmatter block (---\\n...\\n---\\n expected)`);
    }
    const frontmatter = parseYaml(m[1]);
    const body = m[2].trim();

    const article = {
      ...frontmatter,
      format: 'markdown',
      bodyMarkdown: body,
      sourcePath: `content/blog/${file}`,
    };

    validateArticle(article, file);
    articles.push(article);
    console.log(`  ✓ ${file} → ${article.slug} (${body.split(/\s+/).length} words)`);
  }

  writeGeneratedFile(articles);
  console.log(`[generate-blog-md-articles] wrote ${articles.length} article(s) to ${path.relative(REPO_ROOT, OUTPUT_FILE)}`);
}

function writeGeneratedFile(articles) {
  const header = `// AUTO-GENERATED by scripts/generate-blog-md-articles.js — DO NOT EDIT.
// Source: content/blog/*.md
// Run \`npm run generate-md-articles\` to refresh.

import type { BlogArticleData } from '@/components/blog'

export const MD_ARTICLES_DATA: BlogArticleData[] = ${JSON.stringify(articles, null, 2)}
`;
  fs.writeFileSync(OUTPUT_FILE, header);
}

main();
